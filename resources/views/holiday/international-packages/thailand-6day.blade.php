                                    <p class="para-677">After breakfast at the hotel, head to Wat Phra Si Rattana Mahathat to join the locals in their morning
                                        prayers and offerings in this historical temple, which houses one of Thailand’s most revered
                                        Buddha statues. Depart then for Sukhothai to explore this ancient city packed with historical
                                        landmarks, the most impressive being the Sukhothai Historical Park, a UNESCO-listed World
                                        Heritage Site. Admire the ruined temples and palaces as well as the Buddha statues of all sizes.
                                        Lunch en route at a local restaurant. A short drive away is Si Satchanalai, which boasts the
                                        evocative ruins of half a dozen temples, including Wat Phra Si Rattana Mahathat – which is
                                        reached by crossing a wooden suspension bridge – as well as countless statues of Buddha and
                                        elephants. This usually quiet location offers a special spiritual interlude to the tour before arriving in
                                        Lampang for the evening. Overnight in hotel in Lampang Lunch included
