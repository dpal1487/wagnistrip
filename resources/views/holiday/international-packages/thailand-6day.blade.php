                                    <p class="para-677">After breakfast, explore the wonderful Wat Phra That Lampang Luang, just a brief drive from
                                        Lampang, and marvel at the elaborate decorations and beautiful Buddha statues. Continue north,
                                        stopping briefly at Phayao Lake for a coffee break. Then it’s on to the southern outskirts of Chiang
                                        Rai to visit Wat Rong Khun, a dazzling temple built in the 1990s. Its ornate all-white exterior, with
                                        embedded pieces of mirror, gives it a resplendent appearance, making it one of the most beautiful
                                        temples in Thailand. Continue to Chiang Rai for lunch. Afterwards, head to the Golden Triangle,
                                        where Thailand, Laos and Myanmar meet. This natural wilderness gained an unwanted notoriety in
                                        the 1960s because of the abundant opium cultivation in the area. Then enjoy a boat trip around the
                                        Golden Triangle with a stop at Don Sao Island, which forms part of Laos (boat trip lasts approx. 45
                                        minutes). After, visit the 212 House of Opium and stroll around its rare collection of opium
                                        paraphernalia such as weights, scales and pipes. Learn the history of this drug and about the
                                        devastating effect its trade had on local communities. Overnight in Chiang Rai Breakfast and lunch
                                        included
                                    </p>
